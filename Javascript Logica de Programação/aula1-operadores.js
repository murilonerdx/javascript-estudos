// Operadores de compração
// > maior que
// >= maior que ou igual a
// < menor que
// <= menor ou igual a
// == igualdade (valor)
// === igualdade ( tipo do valor)
// != diferente que
// !== diferente do tipo do valor

console.log(10 > 5); //true
console.log(10 >= 5); //true
console.log(10 < 5);
console.log(10 <= 5);
console.log(10 == 5);
console.log(10 === 5);
console.log(10 !== 5); //true
console.log(10 != 5); //true

